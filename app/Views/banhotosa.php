    <h2>Animal de estimação sempre vai precisar de banho e tosa, né?</h2>
    <img id="cachoro" src="../Imagens/cachoro.png" alt="">
    <p>Se você possui um animalzinho em casa, com certeza já ouviu falar da tosa higiênica, um serviço muito comum em pet shops. Ela contribui mantendo a higiene e a saúde do animal em dia, sendo realizada nas regiões da barriga, perianal e anal, e almofadas plantares.</p>
    <H2>Qual a importância desse processo?</H2>
    <p>A tosa higiênica se torna necessária de acordo com a raça do cão, por exemplo, a tosa higiênica em yorkshire. Esse tipo de procedimento não é um luxo, como muitos pensam, mas sim um cuidado indispensável para cortar o excesso de pelo dessa raça.
        
        Cada região tosada possui uma necessidade de depilação óbvia, como a tosa das partes inferiores das patas, onde a remoção do pelo acontece para evitar que o cão ou gato escorregue na própria pelagem. Além disso, também pode ser feita uma tosa estética juntamente da tosa necessária.
        
        Esse é o caso da famosa tosa higiênica de poodle, onde ocorre a remoção do excesso de pelos e um corte desenhado na pelagem do animal. Sendo assim, nenhum pet deve ficar de fora da tosa, pois mesmo alguns animaizinhos de pelo curto acabam se beneficiando desses cuidados.
        
        Curiosamente, apesar das diversas opções dentro do pacote de tosa, nem todos os bichinhos requisitam o conjunto completo de cuidados. Alguns tutores acreditam que a remoção de muitos pelos pode causar problemas, deixando a pele do pet desprotegida.</p>
       <h2>Qual o valor de banho e tosa?</h2>
        <p>O serviço de tosa com tesoura apresenta preços que diferem até R$ 80, de R$ 70 até R$ 150. Já a tosa simples apresenta preços de R$ 45 até R$ 120. O banho com hidratação tem ofertas entre R$ 35 e R$ 100. A tosa Higiênica pode custar de R$ 45 até R$ 80.</p>
